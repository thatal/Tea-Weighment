     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules = Vehicle::rules();
        $this->validate($request, $rules);
        try {
            Vehicle::create($request->only(["name", "weight"]));
            return response()->json([
                "status"  => true,
                "message"   => "Successfully stored."
            ]);
        } catch (\Throwable $th) {
            return response()->json([
                "status"    => false,
                "message"   => "Failed."
            ], 402);
        }
    }

